/*POO

Los paradigmas principalmente que dan paso a las POO son con estados, junto con el imperactivo y el declarativo

Pilares de la programacion orientada a objetos

Abtraccion: oculta detalles internos y muestra las caracteristicas relevante.

Encapsulamiento: protege los datos de un objeto para evitar accesos indebidos, promoviendo el uso de metodos publicos para interactuar con ellos 

herencia: permite que una clase reutilice los atributos y metodos de otra, promoviendo la extencion y personalizacion.

Polimorfismo: habilidad de un metodo o funcion para comportarse de diferentes maneras segun el objetp o los argumentos con los que interactua.

*/
